#include "libft.h"

void		*ft_memcpy(void *s1, const void *s2, size_t n)
{
	char	*temp1;

	temp1 = s1;
	if (n != 0 && s1 != s2)
	{
		while (n--)
		{
			*temp1++ = *(char *) s2++;
		}
	}
	return (s1);
}
