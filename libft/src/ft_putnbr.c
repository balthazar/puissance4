#include "libft.h"

void	ft_putnbr(int n)
{
	if (n > 2147483647 || n < -2147483647)
		return ;
	if (n < 0)
	{
		n = n * (-1);
		ft_putchar('-');
		return (ft_putnbr(n));
	}
	if (n > 9)
		ft_putnbr(n / 10);
	ft_putchar('0' + (n % 10));
}
